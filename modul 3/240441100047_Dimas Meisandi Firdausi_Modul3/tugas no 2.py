# Meminta pengguna memasukkan angka
angka = input("Masukkan angka bulat: ")

# Inisialisasi variabel untuk menyimpan hasil angka terbalik
angka_terbalik = ""

# Menggunakan loop untuk membalikkan urutan angka
for karakter in angka:
    angka_terbalik = karakter + angka_terbalik

# Menampilkan hasil
print(f"Angka terbalik: {angka_terbalik}")









