# Nilai tukar USD ke IDR
kurs_usd_to_idr = 15261.93  # Kurs sesuai dengan tanggal praktikum
# Nominal uang yang dimiliki Suraji dalam USD
usd_amount = 35

# Menjumlahkan uang yang didapat dalam IDR
idr_amount = usd_amount * kurs_usd_to_idr

# Menampilkan hasil
print(f"Nominal uang yang didapatkan Suraji dalam Rupiah: Rp {idr_amount:,.2f}")
